# looping map
knights = {'gallahad': 'the pure', 'robin': 'the brave'}
for k, v in knights.items():
    print(k, v)

# looping with index as well
for i, v in enumerate(['tic', 'tac', 'toe']):
    print(i, v)

# looping collection in reverse
for i in reversed([4, 5, 6, 7]):
    print(i)

# looping in sorted manner
for f in sorted([5, 4, 3, 2, 1]):
    print(f)
